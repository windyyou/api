package com.fnst.cloudapi.pojo.openstackapi.foros;

public class Links {
	private String href;

	private String rel;

	public void setHref(String href) {
		this.href = href;
	}

	public String getHref() {
		return this.href;
	}

	public void setRel(String rel) {
		this.rel = rel;
	}

	public String getRel() {
		return this.rel;
	}

}